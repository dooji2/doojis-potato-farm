package com.dooji.dpf;

public class Potato {
    private final int x;
    private final int y;
    private final int size;

    public Potato(int x, int y, int size) {
        this.x = x;
        this.y = y;
        this.size = size;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getSize() {
        return size;
    }
}